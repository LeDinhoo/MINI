#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*s1;
	int		i;

	i = 0;
	s1 = (char *) s;
	while (n > 0)
	{
		s1[i] = c;
		i++;
		n--;
	}
	return (s1);
}
